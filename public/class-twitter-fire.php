	}
	
	function remove_cron(){
		wp_clear_scheduled_hook( 'do_get_tweets' );
	}

	function cron_add_get_tweets($schedules){
// 			error_log('TWITTERFIRE - cron_add_get_tweets()');
		$schedules['check_tweets'] = array(
		'interval' => 300, //every 5 minutes 
		'display' => __( 'Check tweets cron' )
		);
		return $schedules;
	}

	function get_tweets(){
		$last_fetch = get_option('twitterfire_last_api_fetch', 0);
		
		if($last_fetch && time() < $last_fetch){
			return;
		}
		
		update_option('twitterfire_last_api_fetch', time()+600);
		
		
		
		$tobj =  new Twitterfier();
		$options = get_option($this->plugin_slug.'_settings',array());

		$tobj->init(
				$options['twitter_consumer_key_text'],
				$options['twitter_consumer_secret_text']
			);


		$tobj->get_tweets(array('screen_name'=>$options['twitter_screen_name_text']));


	}

